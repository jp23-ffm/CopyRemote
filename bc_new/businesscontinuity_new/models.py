class ImportStatus(models.Model):
    date_import = models.DateTimeField(auto_now_add=True)
    success = models.BooleanField(default=False)
    message = models.TextField(blank=True, null=True)
    nb_entries_created = models.IntegerField(default=0)
    source_url = models.URLField(blank=True, null=True)

    def __str__(self):
        return f"{'OK' if self.success else 'KO'} {self.date_import.strftime('%d.%m.%Y %H:%M')}"


class BCRuleTexts(models.Model):
    """
    Stores the user-defined action texts for each BC decision rule.
    One row per rule_key — updated in place each time the wizard is applied.
    A single set of texts is shared across all datacenters (one DR at a time).
    """

    rule_key    = models.CharField(
        max_length=50,
        unique=True,
        db_index=True,
        help_text="BC rule identifier — matches constants in bc_rules_logic.py",
    )
    action_text = models.TextField(
        help_text="Text written into action_during_lp for servers matching this rule",
    )
    updated_at  = models.DateTimeField(auto_now=True)
    updated_by  = models.CharField(
        max_length=150,
        blank=True,
        help_text="Username of the last editor",
    )

    class Meta:
        verbose_name        = "BC Rule Text"
        verbose_name_plural = "BC Rule Texts"
        ordering            = ["rule_key"]

    def __str__(self):
        return f"{self.rule_key}: {self.action_text[:60]}"
