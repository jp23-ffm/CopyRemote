from django.db import models

class ServerUnique(models.Model):

    # Represents the unique server, identified by his hostname.

    hostname = models.CharField(max_length=255, unique=True, verbose_name="Unique hostname")
    
    priority_asset = models.CharField(max_length=255, blank=True, null=True, verbose_name="Priority Asset")
    in_live_play = models.CharField(max_length=255, blank=True, null=True, verbose_name="In Live Play")
    action_during_lp = models.TextField(blank=True, null=True, verbose_name="Action during Live Play")
    action_during_lp_history = models.TextField(blank=True, null=True, verbose_name="Action during Live Play History")
    original_action_during_lp = models.TextField(blank=True, null=True, verbose_name="Original Action During Live Play")
    original_action_during_lp_history = models.TextField(blank=True, null=True, verbose_name="Original Action During Live Play History")
    cluster = models.CharField(max_length=255, blank=True, null=True, verbose_name="Cluster")
    cluster_type = models.CharField(max_length=255, blank=True, null=True, verbose_name="Cluster Type")
    global_history = models.JSONField(default=list, help_text="Historical entries")

    def __str__(self):
        return self.hostname

    def append_global_history(self, changes: dict, username: str, source: str) -> None:
        """
        Append a structured entry to global_history.
        Matches the format produced by BusinessContinuityInformationForm.save()
        so that the history view is consistent regardless of the edit source.

        Args:
            changes  : dict of field-level change descriptors, e.g.:
                       { 'priority_asset': {'old': 'YES', 'new': 'NO'} }
                       { 'action_during_lp': {'info': 'Updated, check its history'} }
            username : user or process name (e.g. request.user.username, 'BC Rules')
            source   : human-readable source label:
                       'Single Edit' | 'Bulk Edit' | 'CSV Import' |
                       'Application CSV Import' | 'BC Rules'

        Notes:
            - No-ops silently if changes is empty.
            - Caps global_history at 500 entries to prevent unbounded growth.
            - Does NOT call self.save() — caller is responsible for saving,
              typically via bulk_update(..., ['global_history']).
        """
        import datetime

        if not changes:
            return

        entry = {
            'date':       datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S'),
            'user':       username,
            'fromsource': source,
            'changes':    changes,
        }

        gh = self.global_history if isinstance(self.global_history, list) else []
        gh.insert(0, entry)
        self.global_history = gh[:500]

    class Meta:
        verbose_name = "Unique server"
        verbose_name_plural = "Unique servers"


